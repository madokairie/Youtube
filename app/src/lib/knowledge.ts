export function getKnowledge(blogType: "marketing" | "beauty"): string {
  return blogType === "marketing" ? MARKETING_KNOWLEDGE : BEAUTY_KNOWLEDGE;
}

export function getPlatformRule(
  platform: "note" | "x_long" | "x_post",
  blogType: "marketing" | "beauty"
): string {
  return PLATFORM_RULES[platform][blogType];
}
